#demonstra a utilização da estrutura de repetição for

# imprimir no console cada caracter do texto: Olá, laço for.

# define o valor do texto que será impresso cada caracter
texto = "Olá, laço for."

# imprime cada caracter do texto
for item in texto:
    print("Caractere: ", item)

# imprimir cada número do intervalo: 1 a 9
for numero in range(1,10):
    print("Número do intervalo: " + str(numero))
